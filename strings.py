# Defining a Variable

name = "python school"
print(name.title()) #Converting to title case

print(name.upper()) #Converting to upper case
print(name.lower()) #Converting to lower case

first_name = 'Python'
last_name = 'School'

full_name = first_name + " " + last_name #concatenation
print(full_name)

print("Hello , " + full_name.title() + " !")

print("Languages : \n\t Python \n\t C \n\t C++") #\n for newline \t for tabspace

fav_language = " python " #extra whitespace
print(fav_language.rstrip()) #removes space from right
print(fav_language.lstrip()) #removes space from left
print(fav_language.strip()) #removes space from both the sides

